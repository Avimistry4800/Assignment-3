// #2 budgetCalculator function
function budgetCalculator (watch,mobile,laptop){
    var watchCost = watch * 50 ;
    var mobileCost = mobile * 100;
    var laptopCost = laptop *500;
    var totalCost = watchCost + mobileCost + laptopCost ;
    return totalCost ;
}
